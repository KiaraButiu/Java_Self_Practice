package day04_selection_statements.lab;
// Write an if statement that assigns 5 to x when y is equal to 20

public class IfStatement2 {
    public static void main(String[] args) {

        int x = 0;
        int y = 20;

        if (y == 20){
            x = 5;
        }
        System.out.println("x = " + x);

    }
}

